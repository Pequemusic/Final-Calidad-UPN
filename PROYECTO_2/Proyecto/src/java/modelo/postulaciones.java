package modelo;

/**
 *
 * @author VB
 */
public class postulaciones {
    private int id;
    private String fecha;
    private String dni;

    public postulaciones(int id, String fecha, String dni) {
        this.id = id;
        this.fecha = fecha;
        this.dni = dni;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }
    
    
}
